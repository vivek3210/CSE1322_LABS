class Calculator {
    public float add(float i, float j){
        float sum = (i+j);
        return sum;
    }
    public float subtract(float i, float j){
        float difference = (i-j);
        return difference;
    }
    public float multiply(float i, float j){
        float product = (i*j);
        return product;
    }
    public float divide(float i, float j){
        float quotient = (i/j);
        return quotient;
    }
}
